#!/usr/bin/env bash
# claude-executor/bin/test-dup-audit.sh
#
# 全 vitest test file の test name を scan し、複数 file に出現する重複名を
# Haiku に判定させて logs/test-dup-audit.md に Markdown レポート出力する。
#
# Usage:
#   claude-executor/bin/test-dup-audit.sh
#
# 設計方針:
#   - 削除は一切行わない (audit のみ) → 削除 PR は別途人が決定
#   - Haiku で安く広く scan (= consumer-system の Haiku 分岐 pattern を流用)
#   - 重複 pattern を CONSOLIDATE / KEEP / DELETE の 3 分類
#
# 環境変数:
#   TEST_DUP_AUDIT_MODEL        使用モデル (default: claude-haiku-4-5-20251001)
#   TEST_DUP_AUDIT_TIMEOUT_SEC  claude timeout (default: 600 秒)
#   TEST_DUP_AUDIT_MIN_COUNT    重複と見なす最小 file 数 (default: 2)

set -uo pipefail

REPO="$(cd "$(dirname "$0")/.." && pwd)"
OUT="$REPO/logs/test-dup-audit.md"
LOG_FILE="$REPO/logs/test-dup-audit-$(date +%Y%m%d-%H%M%S).log"
MODEL="${TEST_DUP_AUDIT_MODEL:-claude-haiku-4-5-20251001}"
TIMEOUT_SEC="${TEST_DUP_AUDIT_TIMEOUT_SEC:-600}"
MIN_COUNT="${TEST_DUP_AUDIT_MIN_COUNT:-2}"
LOCKFILE="/tmp/manademia-test-dup-audit.lock"

mkdir -p "$REPO/logs"
log() { echo "[$(date +%H:%M:%S)] $*" | tee -a "$LOG_FILE"; }

# === Lock (single instance) ===
if [[ -f "$LOCKFILE" ]]; then
  OLD_PID="$(cat "$LOCKFILE" 2>/dev/null || echo '')"
  if [[ -n "$OLD_PID" ]] && kill -0 "$OLD_PID" 2>/dev/null; then
    log "another test-dup-audit running (PID=$OLD_PID), exit"
    exit 0
  fi
  rm -f "$LOCKFILE"
fi
echo "$$" > "$LOCKFILE"
trap 'rm -f "$LOCKFILE" "$DUP_DATA_FILE" "$DETAILS_FILE" 2>/dev/null' EXIT

# === Step 1: test name 抽出 + 重複 grouping ===
log "Step 1: scanning test files for duplicate test names..."

DUP_DATA_FILE="/tmp/manademia-test-dup-data.txt"
DETAILS_FILE="/tmp/manademia-test-dup-details.txt"
: > "$DUP_DATA_FILE"
: > "$DETAILS_FILE"

# `it("...")` / `test("...")` の string literal を抽出 (10-80 文字、" ` ' いずれの quote も対応)
find "$REPO" \( -name "*.test.ts" -o -name "*.test.tsx" \) \
  -not -path "*/node_modules/*" \
  -not -path "*/.next/*" \
  -not -path "*/.claude/worktrees/*" \
  -print0 2>/dev/null \
  | xargs -0 grep -hE '^[[:space:]]+(it|test)\(' 2>/dev/null \
  | sed -E "s/^[[:space:]]+(it|test)\([[:space:]]*[\"\`']([^\"\`']{10,80})[\"\`'].*/\2/" \
  | grep -vE '^[[:space:]]*(it|test)\(' \
  | sort \
  | uniq -c \
  | sort -rn \
  | awk -v min="$MIN_COUNT" '$1 >= min { sub(/^[[:space:]]+/, ""); print }' \
  > "$DUP_DATA_FILE"

DUP_COUNT="$(wc -l < "$DUP_DATA_FILE")"
log "  found $DUP_COUNT duplicate-name patterns (min files = $MIN_COUNT)"

if [[ "$DUP_COUNT" -eq 0 ]]; then
  log "no duplicates found, writing empty report"
  cat > "$OUT" <<EOF
# Test Duplication Audit Report — $(date '+%Y-%m-%d')

> Generated by claude-executor/bin/test-dup-audit.sh (model: ${MODEL})

No duplicate test names found across files (min count = ${MIN_COUNT}).
EOF
  exit 0
fi

# === Step 2: 各 pattern の出現 file 一覧 (top 10) ===
log "Step 2: locating files for each duplicate pattern..."

while IFS= read -r line; do
  count="$(echo "$line" | awk '{print $1}')"
  name="$(echo "$line" | sed -E 's/^[[:space:]]*[0-9]+[[:space:]]+//')"
  printf '\n## "%s" (%s files)\n' "$name" "$count" >> "$DETAILS_FILE"
  # F = fixed string (regex 解釈しない、test name に special char あっても OK)
  grep -lFrE --include="*.test.ts" --include="*.test.tsx" -- "$name" "$REPO" 2>/dev/null \
    | grep -vE 'node_modules|\.next|\.claude/worktrees' \
    | sed "s|^$REPO/||" \
    | head -10 >> "$DETAILS_FILE"
done < "$DUP_DATA_FILE"

# === Step 3: Haiku に判定を投げる ===
log "Step 3: invoking $MODEL to classify (timeout ${TIMEOUT_SEC}s)..."

RAW_PATTERNS="$(cat "$DUP_DATA_FILE")"
RAW_DETAILS="$(cat "$DETAILS_FILE")"

PROMPT_BODY=$(cat <<EOF
あなたは manademia の test refactor audit sub-agent (Haiku) です。
全 vitest test file を scan し、**同名の test name が複数 file に出現** する
pattern を一覧化しました。これらを分類し、Markdown レポートを生成してください。

## 入力データ

### 重複 pattern (count + test name)

\`\`\`
${RAW_PATTERNS}
\`\`\`

### 各 pattern の出現 file 一覧

${RAW_DETAILS}

## 判定タスク

各 pattern について以下のいずれかに分類:

1. **CONSOLIDATE** = 全 file で同じ振る舞いを test している → 共通 helper / contract test に統合すべき
2. **KEEP** = 偶然同名だが、各 file で test 対象 entity / 入力が異なる → 削除すると regression リスク
3. **DELETE** = この test 自体が無価値 (framework の挙動 test、型で表現済、mock の挙動だけ test)

### 判定の助け

- 必要なら Read tool で test file 中身を確認 (最大 3 file / pattern、token 節約のため)
- file 名と test name から推測できる場合は推測で OK
- 自信なければ KEEP 寄り (= 削除 risk を避ける)

## 出力先

\`/home/neo/manademia/logs/test-dup-audit.md\` に Write tool で以下の形式で **直接書き込み** してください:

\`\`\`md
# Test Duplication Audit Report — \$(today)

> Generated by claude-executor/bin/test-dup-audit.sh
> Model: ${MODEL}
> 検査対象: ${DUP_COUNT} duplicate-name patterns

## Executive Summary

- 重複 pattern 検出数: ${DUP_COUNT}
- CONSOLIDATE 推奨: N (= ~X tests 削減可)
- KEEP (誤検知): N
- DELETE 推奨: N (= ~Y tests 削減可)
- **合計削減可能 test 数: ~Z**

## CONSOLIDATE 推奨

### "<test name>"
- 出現: N files
- 提案統合先: <新規 file path / 既存 shared test>
- 削減 tests: N → 1 (= N-1 削減)
- 統合方針: <1-2 文>
- 安全性 risk: low / medium / high

(全 CONSOLIDATE pattern を繰り返し)

## KEEP (誤検知 / 偶然同名)

### "<test name>"
- 出現: N files
- 理由: <なぜ偶然同名と判定したか>

## DELETE 推奨

### "<test name>"
- 出現: N files
- 理由: <なぜ無価値と判定したか>

## Notes
- 本 audit は自動生成、削除前に必ず人が再 review すること
- Next action: 削除対象を spec 化して consumer queue に投入
\`\`\`

\${today} は実行日 (YYYY-MM-DD) に置き換えてください。

## 禁止
- file を実際に **削除しない** (audit のみ)
- 推測で言い切らない (= 自信なければ KEEP 寄り)
- Markdown 以外の format (JSON / yaml 等) で出力
- レポート以外の場所への書き込み (= /tmp や別 path)
EOF
)

# Claude (Haiku) 起動
timeout --signal=TERM --kill-after=30s "${TIMEOUT_SEC}s" \
  claude \
    --model "$MODEL" \
    --dangerously-skip-permissions \
    --print \
    "$PROMPT_BODY" \
  2>&1 | tee -a "$LOG_FILE"
RC=${PIPESTATUS[0]}

log "claude finished (rc=$RC)"

# === Step 4: 後始末 + 検証 ===
if [[ ! -f "$OUT" ]]; then
  log "WARN: report file not written by claude (Haiku may have failed)"
  exit 1
fi

if ! head -1 "$OUT" | grep -qE '^# Test Duplication Audit Report'; then
  log "WARN: report header unexpected, manually check $OUT"
fi

log "DONE. Report: $OUT (raw patterns logged: $LOG_FILE)"
exit 0
